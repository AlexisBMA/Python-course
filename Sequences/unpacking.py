# Unpacking a tuple
x, y, z = 1, 2, 76
print(x)
print(y)
print(z)

# Another example of unpacking a tuple.
data = (1, 2, 76)  # data represents a tuple
x, y, z = data
print(x)
print(y)
print(z)

# We can unpack the values from any sequence type
print("Unpacking a list")
data_list = [12, 13, 14]
p, q, r = data_list
print(p)
print(q)
print(r)


